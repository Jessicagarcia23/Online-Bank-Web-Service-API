    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        Account a = new Account();
       
        a.setCurrentBalance(0);
        a.setType(accountType.getSelectedItem().toString());
        a.setSortCode(445577);
        a.setCustomerNo(currentCustomer);

        String url = "http://localhost:8080/BankingSystemOfficial/api/account";
        Client client = Client.create();
        WebResource resource = client.resource(url);

        System.out.println(new JSONObject(a)); 
        
        ClientResponse response = resource
                .accept(MediaType.APPLICATION_JSON)
                .type(MediaType.APPLICATION_JSON)
                .post(ClientResponse.class, new JSONObject(a).toString());

        if (response.getStatus() == 204) {
          JOptionPane.showMessageDialog(null, "Account has been created ");
        } else {
            JOptionPane.showMessageDialog(null, "Unable to process the request ");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

